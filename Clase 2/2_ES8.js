// JS y ES8

const usuario = {
    name: 'Andres',
    email: 'andres@gmail.com',
    age: 25
};

// Esto devuelve un array con otro array con las entradas. Campos y registros.
console.log(Object.entries(usuario));

// Keys. Los campos.
console.log(Object.keys(usuario));

// Values. Los registros.
console.log(Object.values(usuario));

